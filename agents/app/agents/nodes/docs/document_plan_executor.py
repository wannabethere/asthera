    async def _analyze_document_metadata(self, 
                                       step: DocumentPlanStep,
                                       retrieval_result: DocumentRetrievalResult) -> Dict[str, Any]:
        """Analyze document metadata for comparative analysis"""
        
        focus = step.parameters.get("focus", "document_types")
        group_by = step.parameters.get("group_by", "source_type")
        target_docs = self._get_target_documents(step, retrieval_result)
        
        metadata_analysis = {
            "focus": focus,
            "group_by": group_by,
            "total_documents": len(target_docs),
            "grouped_metadata": {}
        }
        
        # Group documents by specified field
        groups = {}
        for doc in target_docs:
            group_key = getattr(doc, group_by, "unknown")
            if group_key not in groups:
                groups[group_key] = []
            groups[group_key].append({
                "id": str(doc.document_id),
                "type": doc.document_type,
                "created_at": doc.created_at.isoformat() if doc.created_at else None,
                "domain_id": doc.domain_id
            })
        
        metadata_analysis["grouped_metadata"] = groups
        
        return metadata_analysis
    
    async def _extract_comparative_data(self, 
                                      step: DocumentPlanStep,
                                      retrieval_result: DocumentRetrievalResult) -> Dict[str, Any]:
        """Extract data for comparison across documents"""
        
        query = step.parameters.get("query", "")
        comparison_fields = step.parameters.get("comparison_fields", ["content"])
        target_docs = self._get_target_documents(step, retrieval_result)
        
        comparative_data = {
            "query": query,
            "comparison_fields": comparison_fields,
            "documents": []
        }
        
        for doc in target_docs:
            doc_data = {
                "id": str(doc.document_id),
                "type": doc.document_type,
                "source": doc.source_type,
                "comparison_data": {}
            }
            
            for field in comparison_fields:
                if field == "content":
                    doc_data["comparison_data"]["content"] = doc.content[:1000]
                elif field == "metadata":
                    doc_data["comparison_data"]["metadata"] = {
                        "created_at": doc.created_at.isoformat() if doc.created_at else None,
                        "domain_id": doc.domain_id,
                        "created_by": doc.created_by
                    }
            
            comparative_data["documents"].append(doc_data)
        
        return comparative_data
    
    async def _perform_comparison(self, 
                                step: DocumentPlanStep,
                                retrieval_result: DocumentRetrievalResult) -> Dict[str, Any]:
        """Perform detailed comparison across documents"""
        
        comparison_type = step.parameters.get("comparison_type", "cross_document")
        format_type = step.parameters.get("format", "structured")
        target_docs = self._get_target_documents(step, retrieval_result)
        
        comparison_result = {
            "comparison_type": comparison_type,
            "format": format_type,
            "documents_compared": len(target_docs),
            "comparison_analysis": {
                "similarities": [],
                "differences": [],
                "unique_content": []
            }
        }
        
        # Simple comparison logic - could be enhanced with more sophisticated analysis
        if len(target_docs) >= 2:
            doc1, doc2 = target_docs[0], target_docs[1]
            
            # Find similarities
            similarities = self._find_similarities(doc1.content, doc2.content)
            comparison_result["comparison_analysis"]["similarities"] = similarities
            
            # Find differences
            differences = self._find_differences(doc1.content, doc2.content)
            comparison_result["comparison_analysis"]["differences"] = differences
        
        return comparison_result
    
