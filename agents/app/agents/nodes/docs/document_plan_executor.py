    # Helper methods
    
    def _get_target_documents(self, 
                            step: DocumentPlanStep,
                            retrieval_result: DocumentRetrievalResult) -> List[Document]:
        """Get target documents for a step"""
        
        if not step.target_documents:
            return retrieval_result.documents
        
        # Filter documents by target IDs
        target_docs = []
        target_ids = set(step.target_documents)
        
        for doc in retrieval_result.documents:
            if str(doc.document_id) in target_ids:
                target_docs.append(doc)
        
        return target_docs
    
    def _extract_key_insights(self, content: str) -> List[str]:
        """Extract key insights from document content"""
        # Simple key insight extraction - could be enhanced with NLP
        sentences = content.split('.')
        insights = []
        
        for sentence in sentences[:10]:  # Limit to first 10 sentences
            sentence = sentence.strip()
            if len(sentence) > 20 and any(keyword in sentence.lower() for keyword in 
                                        ['important', 'key', 'significant', 'notable', 'critical']):
                insights.append(sentence)
        
        return insights[:5]  # Return top 5 insights
    
    def _calculate_relevance_score(self, query: str, content: str) -> float:
        """Calculate relevance score between query and content"""
        query_words = set(query.lower().split())
        content_words = set(content.lower().split())
        
        if not query_words or not content_words:
            return 0.0
        
        intersection = len(query_words.intersection(content_words))
        union = len(query_words.union(content_words))
        
        return intersection / union if union > 0 else 0.0
    
    def _extract_relevant_snippet(self, query: str, content: str, max_length: int = 200) -> str:
        """Extract relevant snippet from content"""
        # Find sentences containing query words
        sentences = content.split('.')
        relevant_sentences = []
        
        query_words = set(query.lower().split())
        
        for sentence in sentences:
            sentence_words = set(sentence.lower().split())
            if query_words.intersection(sentence_words):
                relevant_sentences.append(sentence.strip())
        
        # Join and truncate
        snippet = '. '.join(relevant_sentences[:3])
        if len(snippet) > max_length:
            snippet = snippet[:max_length] + "..."
        
        return snippet
    
    def _extract_specific_content(self, query: str, content: str) -> str:
        """Extract specific content based on query"""
        # Simple extraction - look for sentences containing query words
        sentences = content.split('.')
        relevant_sentences = []
        
        query_words = set(query.lower().split())
        
        for sentence in sentences:
            sentence_words = set(sentence.lower().split())
            if query_words.intersection(sentence_words):
                relevant_sentences.append(sentence.strip())
        
        return '. '.join(relevant_sentences[:2])  # Return top 2 relevant sentences
    
    def _find_similarities(self, content1: str, content2: str) -> List[str]:
        """Find similarities between two documents"""
        # Simple similarity detection
        words1 = set(content1.lower().split())
        words2 = set(content2.lower().split())
        
        common_words = words1.intersection(words2)
        return list(common_words)[:10]  # Return top 10 common words
    
    def _find_differences(self, content1: str, content2: str) -> List[str]:
        """Find differences between two documents"""
        # Simple difference detection
        words1 = set(content1.lower().split())
        words2 = set(content2.lower().split())
        
        unique_to_1 = words1 - words2
        unique_to_2 = words2 - words1
        
        return {
            "unique_to_first": list(unique_to_1)[:5],
            "unique_to_second": list(unique_to_2)[:5]
        }
    
    def _has_table_content(self, content: str) -> bool:
        """Check if content has table-like structure"""
        return '|' in content or '\t' in content or '  ' in content
    
    def _has_list_content(self, content: str) -> bool:
        """Check if content has list-like structure"""
        lines = content.split('\n')
        list_indicators = sum(1 for line in lines if line.strip().startswith(('-', '*', '•', '1.', '2.')))
        return list_indicators > 2
    
    def _has_key_value_content(self, content: str) -> bool:
        """Check if content has key-value pair structure"""
        lines = content.split('\n')
        kv_indicators = sum(1 for line in lines if ':' in line and len(line.split(':')) == 2)
        return kv_indicators > 2
    
    def _extract_structured_from_content(self, query: str, content: str) -> Dict[str, Any]:
        """Extract structured data from content based on query"""
        # Simple structured extraction
        return {
            "query": query,
            "extracted_data": "Structured data extracted from content",
            "confidence": 0.8
        }
    
    def _create_document_summary(self, content: str, focus: str, max_length: int) -> str:
        """Create a summary of document content"""
        # Simple summarization - take first few sentences
        sentences = content.split('.')
        summary_sentences = []
        
        for sentence in sentences:
            if len('. '.join(summary_sentences)) + len(sentence) < max_length:
                summary_sentences.append(sentence.strip())
            else:
                break
        
        summary = '. '.join(summary_sentences)
        if len(summary) > max_length:
            summary = summary[:max_length] + "..."
        
        return summary
    
    def _serialize_analysis_result(self, result: DocumentAnalysisResult) -> Dict[str, Any]:
        """Serialize analysis result for JSON output"""
        return {
            "document_id": result.document_id,
            "analysis_type": result.analysis_type,
            "content": result.content,
            "metadata": result.metadata,
            "confidence": result.confidence,
            "key_insights": result.key_insights,
            "structured_data": result.structured_data
        }
    
    async def _generate_final_answer(self, 
                                   plan: DocumentPlan,
                                   step_results: List[ExecutionResult]) -> str:
        """Generate final answer from step results"""
        
        # Simple final answer generation
        if not step_results:
            return "No analysis results available."
        
        # Get the last successful result
        last_result = None
        for result in reversed(step_results):
            if result.success:
                last_result = result
                break
        
        if not last_result:
            return "Analysis failed - no successful steps completed."
        
        # Generate answer based on plan strategy
        if plan.strategy == DocumentPlanningStrategy.COMPREHENSIVE_ANALYSIS:
            return f"Based on comprehensive analysis of {len(plan.retrieval_result.documents)} documents, here's what I found..."
        elif plan.strategy == DocumentPlanningStrategy.COMPARATIVE_ANALYSIS:
            return f"After comparing {len(plan.retrieval_result.documents)} documents, here are the key differences and similarities..."
        elif plan.strategy == DocumentPlanningStrategy.STRUCTURED_EXTRACTION:
            return f"Here's the structured data extracted from {len(plan.retrieval_result.documents)} documents..."
        else:
            return f"Based on analysis of {len(plan.retrieval_result.documents)} documents, here's the answer to your question..."

